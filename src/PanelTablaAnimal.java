    private void jButtonBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBorrarActionPerformed
        int fila = jTableAnimal.getSelectedRow();
        if (fila == -1) {
            JOptionPane.showMessageDialog(null, "No se seleccion√≥ fila");
        } else {
            idc = Integer.parseInt((String) jTableAnimal.getValueAt(fila, 0).toString());
            try {
                PreparedStatement elim = conet.prepareStatement("DELETE FROM animales WHERE id=' " + idc + "'");
                elim.executeUpdate();
                consultar();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e + "No se logro borrar el registro");
            }

        }
    }//GEN-LAST:event_jButtonBorrarActionPerformed
    private void consultar() {
        String sql = "SELECT * FROM animales";
        
        try {
            conet = con.getConexion();
            st = conet.createStatement();
            rs = st.executeQuery(sql);
            Object[] animal = new Object[5];

            model = (DefaultTableModel) jTableAnimal.getModel();
            while (rs.next()) {
                animal[0] = rs.getInt("id");
                animal[1] = rs.getString("especie");
                animal[2] = rs.getString("raza");
                animal[3] = rs.getFloat("peso");
                animal[4] = rs.getString("nombre");
                model.addRow(animal);
            }
            jTableAnimal.setModel(model);
        } catch (SQLException e) {
            System.out.println(e);
        }
    }

    private void limpiarTabla() {
        for (int i = 0; i <= jTableAnimal.getRowCount(); i++) {
            model.removeRow(i);
        }
    }

