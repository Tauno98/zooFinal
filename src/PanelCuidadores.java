    private void jButtonBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBorrarActionPerformed
        // TODO add your handling code here:
        int fila = jTableCuidadores.getSelectedRow();
        if(fila == -1){
            JOptionPane.showMessageDialog(null, "No se seleccion√≥ fila");
        }else{
            idc = Integer.parseInt((String) jTableCuidadores.getValueAt(fila, 0).toString());
            try{
                PreparedStatement elim = conet.prepareStatement("DELETE FROM cuidadores WHERE id=' "+idc+"'");
                elim.executeUpdate();
                consultar();
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, e + "No se logro borrar el registro");
            }
            
        }
    }//GEN-LAST:event_jButtonBorrarActionPerformed
private void consultar() {
        String sql = "select * from cuidadores";

        try {
            conet = con.getConexion();
            st = conet.createStatement();
            rs = st.executeQuery(sql);
            Object[] cuidadores = new Object[5];

            model = (DefaultTableModel) jTableCuidadores.getModel();
            while (rs.next()) {
                cuidadores[0] = rs.getInt("id");
                cuidadores[1] = rs.getString("nombre");
                cuidadores[2] = rs.getString("apellidos");
                cuidadores[3] = rs.getFloat("salario");
                cuidadores[4] = rs.getInt("especialidad");
                model.addRow(cuidadores);
            }
            jTableCuidadores.setModel(model);
        } catch (SQLException e) {
        }
    }

