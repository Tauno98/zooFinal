    private void jButtonBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBorrarActionPerformed
      int fila = jTableCuidados.getSelectedRow();
        if(fila == -1){
            JOptionPane.showMessageDialog(null, "No se seleccion√≥ fila");
        }else{
            idc = Integer.parseInt((String) jTableCuidados.getValueAt(fila, 0).toString());
            try{
                PreparedStatement elim = conet.prepareStatement("DELETE FROM usuarios WHERE id=' "+idc+"'");
                elim.executeUpdate();
                consultar();
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, e + "No se logro borrar el registro");
            }
            
        }
        
    }//GEN-LAST:event_jButtonBorrarActionPerformed
private void consultar() {
        String sql = "SELECT * FROM cuidados";

        try {
            conet = con.getConexion();
            st = conet.createStatement();
            rs = st.executeQuery(sql);
            Object[] cuidados = new Object[7];

            model = (DefaultTableModel) jTableCuidados.getModel();
            while (rs.next()) {
                cuidados[0] = rs.getInt("id");
                cuidados[1] = rs.getString("Comida");
                cuidados[2] = rs.getString("Cantidad comida");
                cuidados[3] = rs.getFloat("coste");
                cuidados[4] = rs.getString("Habitat");
                cuidados[5] = rs.getInt("Animal");
                cuidados[6] = rs.getInt("Cuidador");
                model.addRow(cuidados);
            }
            jTableCuidados.setModel(model);
        } catch (SQLException e) {
        }
    }

    private void limpiarTabla() {
        for (int i = 0; i <= jTableCuidados.getRowCount(); i++) {
            model.removeRow(i);
        }
    }

